package models.task;

import com.fasterxml.jackson.annotation.JsonBackReference;

import javax.persistence.*;

@Entity
@Table(name = "tasktpropertyvalue")
public class TaskPropertyValue {

	@SuppressWarnings("UnusedDeclaration") //generated by hibernate
	@Id
	@SequenceGenerator(name = "tasktpropertyvalue_seq", sequenceName = "tasktpropertyvalue_seq")
	@GeneratedValue(strategy = GenerationType.SEQUENCE, generator = "tasktpropertyvalue_seq")
	private Long id;

	@ManyToOne
	@JoinColumn(nullable = false)
	private TaskProperty property;

	@ManyToOne
	@JoinColumn(nullable = false)
	@JsonBackReference
	private TaskTemplate taskTemplate;

	private String value;

	public Long getId() {
		return id;
	}

	public TaskProperty getProperty() {
		return property;
	}

	public void setProperty(TaskProperty property) {
		this.property = property;
	}

	public TaskTemplate getTaskTemplate() {
		return taskTemplate;
	}

	public void setTaskTemplate(TaskTemplate taskTemplate) {
		this.taskTemplate = taskTemplate;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
}
