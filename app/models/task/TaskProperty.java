package models.task;

import javax.persistence.*;

@Entity
@Table(name = "tasktproperty")
public class TaskProperty {
	@SuppressWarnings("UnusedDeclaration") //generated by hibernate
	@Id
	@SequenceGenerator(name = "tasktproperty_seq", sequenceName = "tasktproperty_seq")
	@GeneratedValue(strategy = GenerationType.SEQUENCE, generator = "tasktproperty_seq")
	private Long id;

	private String name;

	public Long getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
